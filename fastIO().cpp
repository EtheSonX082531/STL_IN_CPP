#include <bits/stdc++.h>
using namespace std;

#define fastIO() ios::sync_with_stdio(0); cin.tie(0);

int main() {
    fastIO();  // Enable fast input/output

    int n;
    cin >> n;  // Fast input
    cout << "Number: " << n << endl;  // Fast output

    return 0;
}

/*

Why Use It?

Speeds up reading and writing significantly.

Useful when handling large input/output (e.g., 10^5 or 10^6 operations).

Prevents cin from flushing cout, avoiding unnecessary slowdowns.

*/
